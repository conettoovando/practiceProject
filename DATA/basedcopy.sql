CREATE DATABASE usuarios;	
use usuarios; 

CREATE TABLE log ( 
	id INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	nombre VARCHAR(30) NOT NULL,
	apellido VARCHAR(30) NOT NULL,
	rut VARCHAR(50) NOT NULL,
	telefono int (10) NOT NULL,
	edad INT(3) NOT NULL,
	mail VARCHAR(50) NOT NULL,
	n_emergencia int(10) NOT NULL,
	rol VARCHAR(15) NOT NULL,
	password VARCHAR(15) NOT NULL,
	Rutsupervisor VARCHAR(50) NOT NULL
);

CREATE TABLE archivos(
	id INT(255) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	mail VARCHAR(50) NOT NULL,
	nombre VARCHAR(255) NOT NULL,
	bitacora VARCHAR(1000) NOT NULL,
	fecha DATE NOT NULL,
	ruta VARCHAR(255) NOT NULL,
	size VARCHAR(255) NOT NULL
);

CREATE TABLE status(
	id INT(255) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	NombreUsuario VARCHAR(255) NOT NULL,
	Rut VARCHAR(50) NOT NULL,
	Mail VARCHAR(50) NOT NULL,
	Estado VARCHAR(10) NOT NULL,
	Calificacion_JC VARCHAR(1) NOT NULL,
	Calificacion_DC VARCHAR(1) NOT NULL,
	CalificacionCompleta VARCHAR(1) NOT NULL,
	Observacion VARCHAR(255) NOT NULL,
	informef VARCHAR(255) NULL
);

CREATE TABLE consultas(
	id INT(255) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	NombreAlumno VARCHAR(255) NOT NULL,
	RutAlumno VARCHAR(50) NOT NULL,
	MailAlumno VARCHAR(50) NOT NULL,
	Estado VARCHAR(10) NOT NULL,
	NombreSupervisor VARCHAR(255) NOT NULL,
	RutSupervisor VARCHAR(50) NOT NULL,
	MailSupervisor VARCHAR(50) NOT NULL,
	fecha DATE NOT NULL,
	consulta VARCHAR(255) NULL
);

CREATE TABLE SupervisorAlumnos(
	id INT(255) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	NombreSupervisor VARCHAR(255) NOT NULL,
	RutSupervisor VARCHAR(50) NOT NULL,
	MailSupervisor VARCHAR(50) NOT NULL,
	NombreAlumno VARCHAR(255) NOT NULL,
	RutAlumno VARCHAR(50) NOT NULL,
	MailAlumno VARCHAR(50) NOT NULL
);
